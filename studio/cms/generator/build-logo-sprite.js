#!/usr/bin/env node
/**
 * Studio logo sprite builder.
 *
 * Reads every .svg file in studio/assets/images/svg-logos/ (the source of
 * truth for client wordmarks/logomarks) and produces a single sprite file
 * in the same directory at _sprite.svg.
 *
 * Each logo becomes a <symbol id="<name>" viewBox="..."> block containing
 * the source file's path data. Logos are referenced from studio HTML/JS
 * via <use href="/assets/images/svg-logos/_sprite.svg#name"/>. Avatar
 * variants (e.g. for circular testimonial badges) live as separate
 * symbols with a -avatar suffix.
 *
 * Usage:
 *   node build-logo-sprite.js
 *
 * Or via the package.json script:
 *   npm run build:logos
 *
 * Run this whenever logo source files change. The generated sprite is
 * committed to the repo (not built at server-start).
 */

const fs = require("fs");
const path = require("path");

const STUDIO_ROOT = path.resolve(__dirname, "..", "..");
const SRC_DIR = path.join(STUDIO_ROOT, "assets", "images", "svg-logos");
const OUT_FILE = path.join(SRC_DIR, "_sprite.svg");

function extractInner(svgString) {
  const match = svgString.match(/<svg[^>]*>([\s\S]*)<\/svg>/);
  if (!match) {
    throw new Error("could not find <svg>...</svg> in source");
  }
  return match[1].trim();
}

function extractViewBox(svgString) {
  const match = svgString.match(/viewBox="([^"]+)"/);
  if (!match) {
    throw new Error("source SVG missing viewBox");
  }
  return match[1];
}

function buildSprite() {
  if (!fs.existsSync(SRC_DIR)) {
    console.error(`source directory not found: ${SRC_DIR}`);
    process.exit(1);
  }

  const files = fs
    .readdirSync(SRC_DIR)
    .filter((f) => f.endsWith(".svg") && !f.startsWith("_"))
    .sort();

  if (files.length === 0) {
    console.error(`no .svg files found in ${SRC_DIR}`);
    process.exit(1);
  }

  const symbols = files.map((file) => {
    const name = path.basename(file, ".svg");
    const source = fs.readFileSync(path.join(SRC_DIR, file), "utf8");
    const viewBox = extractViewBox(source);
    const inner = extractInner(source);
    return `  <symbol id="${name}" viewBox="${viewBox}">${inner}</symbol>`;
  });

  const sprite = [
    `<?xml version="1.0" encoding="UTF-8"?>`,
    `<!-- Generated by studio/cms/generator/build-logo-sprite.js. Do not edit. -->`,
    `<svg xmlns="http://www.w3.org/2000/svg" style="display:none" aria-hidden="true">`,
    ...symbols,
    `</svg>`,
    ``,
  ].join("\n");

  fs.writeFileSync(OUT_FILE, sprite);

  console.log(`✓ wrote ${files.length} symbols to ${path.relative(STUDIO_ROOT, OUT_FILE)}`);
}

try {
  buildSprite();
} catch (err) {
  console.error(`× sprite build failed: ${err.message}`);
  process.exit(1);
}
