#!/usr/bin/env node
/**
 * Studio icon sprite builder.
 *
 * Reads every .svg file in /assets/images/svg-icons/ (project root, the
 * single source of truth for icon glyphs) and produces a single sprite
 * file at /studio/assets/images/svg-icons/_sprite.svg.
 *
 * Each icon becomes a <symbol id="<name>" viewBox="0 0 24 24"> block
 * containing the source file's path data. Icons are referenced from
 * studio HTML/JS via <use href="/assets/images/svg-icons/_sprite.svg#name"/>.
 *
 * Usage:
 *   node build-icon-sprite.js
 *
 * Or via the package.json script:
 *   npm run build:icons
 *
 * Run this whenever icon source files change. The generated sprite is
 * committed to the repo (not built at server-start).
 */

const fs = require("fs");
const path = require("path");

// Paths relative to this script (studio/cms/generator/build-icon-sprite.js).
const ROOT = path.resolve(__dirname, "..", "..", "..");
const SRC_DIR = path.join(ROOT, "assets", "images", "svg-icons");
const OUT_DIR = path.join(ROOT, "studio", "assets", "images", "svg-icons");
const OUT_FILE = path.join(OUT_DIR, "_sprite.svg");

function extractInner(svgString) {
  // Match anything between the opening <svg ...> tag and the closing </svg>.
  // Source icons have a single root <svg>; nested SVGs are not expected.
  const match = svgString.match(/<svg[^>]*>([\s\S]*)<\/svg>/);
  if (!match) {
    throw new Error("could not find <svg>...</svg> in source");
  }
  return match[1].trim();
}

function extractViewBox(svgString) {
  const match = svgString.match(/viewBox="([^"]+)"/);
  return match ? match[1] : "0 0 24 24";
}

function buildSprite() {
  if (!fs.existsSync(SRC_DIR)) {
    console.error(`source directory not found: ${SRC_DIR}`);
    process.exit(1);
  }

  fs.mkdirSync(OUT_DIR, { recursive: true });

  const files = fs
    .readdirSync(SRC_DIR)
    .filter((f) => f.endsWith(".svg") && !f.startsWith("_"))
    .sort();

  if (files.length === 0) {
    console.error(`no .svg files found in ${SRC_DIR}`);
    process.exit(1);
  }

  const symbols = files.map((file) => {
    const name = path.basename(file, ".svg");
    const source = fs.readFileSync(path.join(SRC_DIR, file), "utf8");
    const viewBox = extractViewBox(source);
    const inner = extractInner(source);
    return `  <symbol id="${name}" viewBox="${viewBox}">${inner}</symbol>`;
  });

  const sprite = [
    `<?xml version="1.0" encoding="UTF-8"?>`,
    `<!-- Generated by studio/cms/generator/build-icon-sprite.js. Do not edit. -->`,
    `<svg xmlns="http://www.w3.org/2000/svg" style="display:none" aria-hidden="true">`,
    ...symbols,
    `</svg>`,
    ``,
  ].join("\n");

  fs.writeFileSync(OUT_FILE, sprite);

  console.log(`✓ wrote ${files.length} symbols to ${path.relative(ROOT, OUT_FILE)}`);
}

try {
  buildSprite();
} catch (err) {
  console.error(`× sprite build failed: ${err.message}`);
  process.exit(1);
}
