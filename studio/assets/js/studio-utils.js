//
//------- Splide Lazy-Loader (single-flight) -------//
//
// Splide CSS + JS are pulled from the CDN on first need. Concurrent callers
// share one in-flight promise so we never inject duplicate <script> tags.

var SPLIDE_JS = "https://cdn.jsdelivr.net/npm/@splidejs/splide@4.1.4/dist/js/splide.min.js";
var SPLIDE_CSS = "https://cdn.jsdelivr.net/npm/@splidejs/splide@4.1.4/dist/css/splide.min.css";
var splidePromise = null;

function ensureSplide(callback) {
  if (typeof Splide !== "undefined") {
    callback();
    return;
  }
  if (!splidePromise) {
    splidePromise = new Promise(function (resolve) {
      if (!document.querySelector('link[href*="splide"]')) {
        var link = document.createElement("link");
        link.rel = "stylesheet";
        link.href = SPLIDE_CSS;
        document.head.appendChild(link);
      }
      var script = document.createElement("script");
      script.src = SPLIDE_JS;
      script.onload = function () { resolve(); };
      document.head.appendChild(script);
    });
  }
  splidePromise.then(callback);
}

//
//------- Studio Content (manifest from CMS) -------//
//
// Generated by studio/cms/generator/ → studio/assets/data/studio-content.json.
// Fetched once and cached for the homepage feed and the next-read card.

var STUDIO_CONTENT = null;
var STUDIO_CONTENT_PROMISE = null;

function loadStudioContent() {
  if (STUDIO_CONTENT) return Promise.resolve(STUDIO_CONTENT);
  if (STUDIO_CONTENT_PROMISE) return STUDIO_CONTENT_PROMISE;
  var url = getStudioPrefix() + "assets/data/studio-content.json";
  STUDIO_CONTENT_PROMISE = fetch(url)
    .then(function (r) { return r.json(); })
    .then(function (data) {
      STUDIO_CONTENT = data;
      return data;
    })
    .catch(function (err) {
      console.warn("studio-content.json not available:", err);
      STUDIO_CONTENT = { articles: [], caseStudies: [] };
      return STUDIO_CONTENT;
    });
  return STUDIO_CONTENT_PROMISE;
}

